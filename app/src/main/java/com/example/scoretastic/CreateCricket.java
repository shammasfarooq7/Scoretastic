package com.example.scoretastic;

public class CreateCricket {



    int Batsman;
    int Bowler;
    int AllRounder;
    int Wkeeper;




    public void setWkeeper(int wkeeper) {
        Wkeeper = wkeeper;
    }

    public void setAllRounder(int allRounder) {
        AllRounder = allRounder;
    }

    public void setBowler(int bowler) {
        Bowler = bowler;
    }

    public void setBatsman(int batsman) {
        Batsman = batsman;
    }

    public int getWkeeper() {
        return Wkeeper;
    }

    public int getAllRounder() {
        return AllRounder;
    }

    public int getBowler() {
        return Bowler;
    }

    public int getBatsman() {
        return Batsman;
    }
}
